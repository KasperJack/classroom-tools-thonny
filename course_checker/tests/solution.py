def add(a, b):
    """Add two numbers"""
    return a + b

def multiply(a, b):
    """Multiply two numbers"""
    return a - b

def sum_list5(numbers):
    """Sum all numbers in a list"""
    return sum(numbers)

def merge_lists(list1, list2):
    """Merge two lists"""
    return list1 + list2

def greet(name):
    """Greet a person"""
